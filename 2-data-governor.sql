/*=============================================================================
  AI CLASSIFICATION WITH TAG MAPPING
  
  Uses CLASSIFICATION_PROFILE with tag_map to:
  1. Auto-detect PII using AI classification
  2. Map detected PII to custom DATA_CLASSIFICATION tag
  3. Enable tag propagation to downstream tables
  
  [COMPLIANCE] Classification levels map to regulations:
    PII       → GDPR Art.4(1), HIPAA PHI, CCPA Personal Information
    RESTRICTED→ GDPR Art.9 (special categories), HIPAA limited dataset
    SENSITIVE → GDPR Art.6 (legitimate interest), PCI DSS Req 3
    INTERNAL  → SOX controls, internal-only business data
    PUBLIC    → No regulatory constraint
=============================================================================*/

USE ROLE HRZN_DATA_GOVERNOR;

CREATE SCHEMA IF NOT EXISTS HRZN_DB.TAG_SCHEMA;
USE SCHEMA HRZN_DB.TAG_SCHEMA;

CREATE OR REPLACE TAG HRZN_DB.TAG_SCHEMA.DATA_CLASSIFICATION
    ALLOWED_VALUES 'PII', 'RESTRICTED', 'SENSITIVE', 'INTERNAL', 'PUBLIC'
    COMMENT = 'Enterprise data classification. GDPR/HIPAA/PCI/CCPA compliance mapping. Propagates to derived tables.'
    PROPAGATE = ON_DEPENDENCY_AND_DATA_MOVEMENT;

/*=============================================================================
  CLASSIFICATION PROFILE WITH TAG MAP
  
  [COMPLIANCE] tag_map semantic_categories → DATA_CLASSIFICATION level:
    EMAIL, SSN, CREDIT_CARD → PII   (GDPR: erasure required; HIPAA: PHI; PCI: encrypt)
    PHONE, DOB             → RESTRICTED (GDPR: Art.9; HIPAA: limited dataset)
    NAME, ADDRESS, CITY    → SENSITIVE  (GDPR: Art.6; PCI DSS Req 3)
    JOB, COMPANY           → INTERNAL   (SOX internal controls)
=============================================================================*/

USE ROLE SYSADMIN;

CREATE OR REPLACE SNOWFLAKE.DATA_PRIVACY.CLASSIFICATION_PROFILE
    HRZN_DB.HRZN_SCH.HRZN_STANDARD_CLASSIFICATION_PROFILE(
