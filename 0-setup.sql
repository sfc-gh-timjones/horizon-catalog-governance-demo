/*=============================================================================
  DIFFERENTIAL PRIVACY — Privacy Policy + Domains on EMPLOYEES
  
  Entity-level privacy: each EMPLOYEE_ID is a protected entity.
  Governor sees exact results. All other roles get noisy aggregates
  with a weekly privacy budget.
