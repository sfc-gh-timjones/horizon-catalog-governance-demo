/*=============================================================================
  EMPLOYEES TABLE — Synthetic Data for Differential Privacy Demo
  
  2000 synthetic employees with numeric columns (salary, bonus, age).
  No external dependencies — generated entirely with GENERATOR + UNIFORM.
=============================================================================*/

CREATE OR REPLACE TABLE HRZN_DB.HRZN_SCH.EMPLOYEES (
    EMPLOYEE_ID INT,
    DEPARTMENT VARCHAR,
    SALARY FLOAT,
    BONUS FLOAT,
    AGE INT,
    YEARS_AT_COMPANY INT
) AS
SELECT
    ROW_NUMBER() OVER (ORDER BY SEQ4()) AS EMPLOYEE_ID,
    CASE MOD(SEQ4(), 6)
        WHEN 0 THEN 'Engineering'
        WHEN 1 THEN 'Sales'
        WHEN 2 THEN 'Marketing'
        WHEN 3 THEN 'Finance'
        WHEN 4 THEN 'Operations'
        WHEN 5 THEN 'Legal'
    END AS DEPARTMENT,
    ROUND(50000 + UNIFORM(0::FLOAT, 150000::FLOAT, RANDOM()), 2) AS SALARY,
    ROUND(UNIFORM(0::FLOAT, 25000::FLOAT, RANDOM()), 2) AS BONUS,
    22 + UNIFORM(0, 43, RANDOM()) AS AGE,
    UNIFORM(0, 25, RANDOM()) AS YEARS_AT_COMPANY
FROM TABLE(GENERATOR(ROWCOUNT => 2000));

