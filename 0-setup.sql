    CASE
      WHEN CURRENT_ROLE() IN ('HRZN_DATA_GOVERNOR', 'HRZN_DATA_ENGINEER', 'ACCOUNTADMIN')
        THEN NO_PRIVACY_POLICY()
      ELSE PRIVACY_BUDGET(BUDGET_NAME => 'analysts')
    END;

